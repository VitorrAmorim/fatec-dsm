print('Olá Mundo!') #Operação de impressão

print('Bom dia, Vitor.')

nome = 'Vitor' #O identificador nome servirá como variável para armazer a String com valor 'Vitor'
print('Bom dia ' + nome) #Concatenando (juntando) a String 'Bom dia' com o valor da variável Nome

nome = 'Gabriel' #O valor da String será atualizado para 'Gabriel'
print('Bom dia ' + nome)

nome = 30 #Particularidade do Python: o mesmo identificador pode armazenar valores de tipos diferentes
print(nome)

